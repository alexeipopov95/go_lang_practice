package main

import "fmt"

// Ciclos! :D

func ____main() {

	// Ciclo for basico condicional.
	for i := 0; i < 10; i++ {
		fmt.Println(i)
	}

	fmt.Println("")

	// Ciclo For While
	counter := 0
	for counter < 10 {
		fmt.Println(counter)
		counter++
	}

	// For forever... and ever... lalala
	// equivalente a while True de python
	counterForevereverever := 0
	for {
		fmt.Println(counterForevereverever)
		counterForevereverever++
	}

}
